package Simple;

public class PrimitiveCasting {
	
	public static void wet() {
		
		 byte q=20;
		 short w=q;
		 int e=w;
		 long r=e;
		 double t=r;
		 System.out.println(t);
		 
		 
		 
		 float y=120.45f;
		 int u= (int) y;
		 short i = (short) u;
		 System.out.println(i);
	}

	public static void main(String[] args) {
		
		 
		PrimitiveCasting.wet();
		
		
	}

}
