package india;

public class _01_ArithmaticOperators {

	public static void main(String[] args) {
		
		//Arithmetic Operators: +,-,*,/,%(mod) 
		
		int a=20,b=5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);// Mod operator to get remainder
		

}
}