package india;

public class _23_StringBufferAndBuilder {

	public static void main(String[] args) {

		StringBuilder sb = new StringBuilder("Velocity");
		
		sb.reverse(); //to reverse the StringBuilder
		
		System.out.println(sb);
		
		
		StringBuffer sbf = new StringBuffer("Katraj");
		
		sbf.append(" Pune");//to join two string buffer.
		
		System.out.println(sbf);
		
		
		
		
		
		
		
		
		
		
	}

}
