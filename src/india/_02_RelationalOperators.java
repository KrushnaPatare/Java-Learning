package india;

public class _02_RelationalOperators {

	public static void main(String[] args) {
		
		//Relational Operators:<,>,==,<=,>=,!=.
		
		int a=11, b=2;
		System.out.println(a==b);  // false
		System.out.println(a!=b);  // true
		System.out.println(a>b);   // true
		System.out.println(a<b);   // false
		System.out.println(a>=b);  // true
		System.out.println(a<=b);  // false

	}

}
