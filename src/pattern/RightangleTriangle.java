package pattern;

public class RightangleTriangle {

	public static void main(String[] args) {
		
		for (int a=1;a<=5;a++)
		{   
			
			for (int j=1;j<=a;j++)
				
				{
				System.out.print(a);
				}
			System.out.println();
		}

		for (int a=1;a<=5;a++)
		{   
			
			for (int j=1;j<=a;j++)
				
				{
				System.out.print("*");
				}
			System.out.println();
		}
		
		
	}

}
