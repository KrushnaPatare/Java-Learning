package pattern;

public class _Pattern_1 {

	public static void main(String[] args) {
		
		for (int c=1;c<=2;c++)
		{
		   for (int a=1;a<=2;a++)
		   {   
			
			for (int j=1;j<=2;j++)
				
				{
				System.out.print(j);
				}
			
		   }
		   System.out.println();
		   
		}
		       
		         
	}

}
